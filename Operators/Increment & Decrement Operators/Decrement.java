class Decrement
{
	public static void main(String[]args)
	{
		int pocket= 50;
		System.out.println(--pocket);
		System.out.println(--pocket);
		System.out.println(pocket--);
		System.out.println(pocket--);
		System.out.println(--pocket);
		System.out.println(--pocket - pocket--);
		System.out.println(pocket);
		System.out.println(pocket-- + --pocket);
		System.out.println(pocket);
	}
}