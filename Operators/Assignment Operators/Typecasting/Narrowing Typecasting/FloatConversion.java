class FloatConversion
{
    public static void main(String[]args)
    {
     
	 float d=44.52135f;

	 long l=(long)d;

	 int i=(int)d;

 	char c=(char)d;

 	short s=(short)d;

 	byte b=(byte)d;



	System.out.println(l);
	System.out.println(i);
	System.out.println(c);
	System.out.println(s);
	System.out.println(b);
  

    }
}