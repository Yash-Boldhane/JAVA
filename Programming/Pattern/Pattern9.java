import java.util.Scanner;
class Pattern9
{
	public static void main(String[]args)
	{
	 Scanner sc=new Scanner(System.in);
	 System.out.println("Enter the Number");
	

	 char n=sc.charAt();
	 

	
	for(int i=1; i<=4; i++)
	{
	
		for(int j=1; j<=4; j++)
		{

		System.out.print(n+ "  " );
		n++;
		

		}

		
	
		System.out.println();


	}

	


	}


}